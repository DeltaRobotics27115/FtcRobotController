package org.firstinspires.ftc.teamcode.output;

public class DrivePosition {
    public int leftFront;
    public int rightFront;
    public int leftBack;
    public int rightBack;
    public DrivePosition(int lf, int rf, int lb, int rb) {
        leftFront = lf;
        rightFront = rf;
        leftBack = lb;
        rightBack = rb;
    }
}
